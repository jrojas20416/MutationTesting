package org.example;

public class Calculator {


    public int sum(int arg1,int arg2){
        return arg1 + arg2;
    }

    public int mul(int arg1, int arg2){
        return arg1 * arg2;
    }

    public double div(double arg1,double arg2){
        return arg1 / arg2;
    }

    public int rest(int arg1, int arg2){
        return arg1 - arg2;
    }
}
